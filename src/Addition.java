public class Addition {
    public static void main(String[] args) {
        int  y = 3 ,z = 12;
        int x =6;
        System.out.println(y-z); //output is -9
        System.out.println(y+z);// output is 15
        System.out.println(x % y); // output is 0
        System.out.println(y%2); // output is 1
        System.out.println(z/2);// output is 6
        System.out.println(y*x);//output is 18
        System.out.println(x + (z + y));//output is 21

    }
}
